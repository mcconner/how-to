<h3>About Trail API</h3>
<p>Trail API is a resource for outdoor recreation locations. It contains data about trails for hiking and mountain biking, camping, skiing, and more. The data can be filtered by 
location name, activity type, or using latitude, longitude, and geocoding for location search. Each result returned by a search will show information including name, description,
a photo, map, directions, and more. </p>

