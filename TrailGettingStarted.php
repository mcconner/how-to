<h3>Using Trail API</h3>
<p>So now that you've decided that you want to incorporate this API into your project, the first step is to request an API key. This can be done by
simply going to <a class="test" href="http://www.trailapi.com/login.php?page=create">Trail API's login page</a> You will need to enter your email, choose a username 
(screen name), and choose an access level. The first two access levels are free. Click 'Create Account' to submit your request. You should receive
an email shortly with your password and a link to follow to log-in to your account.</p>
<img src="http://web.engr.oregonstate.edu/~mcconner/pic_TrailAPIPricing.jpg" height="50%" width="50%"><br>
<p>Once you have received your password and are logged on to your account, go to 'My Account' at the upper right of the screen. </p>
<img src="http://web.engr.oregonstate.edu/~mcconner/pic_TrailAPIMyAccount.jpg" height="40%" width="50%"><br>
<p>On the 'My Account' page, you should be able to view your own API Key in the center of the page. This key will be important to access the data. </p>

<p>Now that your account is set up, let's move on and learn about using the API key to access data. </p>
